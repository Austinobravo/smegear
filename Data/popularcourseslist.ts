const generateRandomDate = () => {
  const start = new Date(2022, 0, 1);
  const end = new Date(2025, 6, 1);
  return new Date(
    start.getTime() + Math.random() * (end.getTime() - start.getTime())
  )
    .toISOString()
    .split("T")[0];
};

